-- Create the raw_materials table
CREATE TABLE IF NOT EXISTS public.raw_materials (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    cas_number TEXT,
    formula TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable Row Level Security
ALTER TABLE public.raw_materials ENABLE ROW LEVEL SECURITY;

-- Create a policy that allows read access to everyone
DROP POLICY IF EXISTS "Allow public read access" ON public.raw_materials;
CREATE POLICY "Allow public read access" ON public.raw_materials
    FOR SELECT TO public
    USING (true);

